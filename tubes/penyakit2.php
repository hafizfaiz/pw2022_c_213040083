<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;"><span style="font-size: medium;">Berikut ini beberapa daftar penyakit menular yang dapat kami rangkum untuk anda :</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">&nbsp;1.&nbsp;Influenza</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Influenza atau yang lebih umum dikenal dengan flu adalah penyakit  menular yang paling umum diderita oleh orang-orang. Influenza ini  disebabkan oleh virus. Virus influenza adalah virus yang setiap waktunya  bermutasi, sehingga sistem imunitas tubuh sulit mendeteksi virus yang  satu ini. Karena sulitnya sistem imun tubuh mendeteksi virus influenza  ini, maka tubuh cenderung lebih mudah terkena flu. Bahkan tubuh dapat  beberapa kali terkena flu dalam waktu yang berdekatan.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Media Penularan</strong></span></p>
<p><span style="font-size: medium;">Flu dapat ditularkan melalui sistem pernapasan juga melalui air  ludah. Maka jika kita berdekatan dengan orang yang sedang flu,  kemungkinan kita tertular flu sangatlah besar. Perantara udara adalah  media penularan flu yang paling cepat.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan&nbsp;</strong></span></p>
<p><span style="font-size: medium;">Menjaga daya tahan tubuh agar tidak mudah terserang virus. Misalnya  dengan makan teratur, istirahat yang cukup, minum air putih sesuai  kebutuhan, berolah raga, dan memiliki gaya hidup yang sehat.Selain itu,  menjaga daya tahan tubuh juga dapat juga didukung dengan asupan vitamin  terutama Vitamin C yang bisa didapatkan di buah-buahan maupun vitamin  yang dijual di toko-toko.</span></p>
<p><span style="font-size: medium;">Pencegahan lainnya adalah dengan menggunakan masker ditempat umum, terutama bagi yang menderita influenza.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">2. Tuberkulosis (TBC)</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Tuberkulosis adalah penyakit infeksi saluran pernapasan yang  disebabkan oleh bakteri basil. Bakteri basil yang menginfeksi adalah  bakteri basil yang sangat kuat. Akibtanya, akan membutuhkan waktu yang  lama untuk mengobati penyakit ini. Bakteri ini 90% cenderung menginfeksi  paru-paru jika dibandingkan dengan organ-organ lainnya pada tubuh  manusia. Penyakit ini biasanya ditandai dengan batuk terus menerus.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<p><span style="font-size: medium;">TBC adalah penyakit yang menyerang pernapasan. Maka penularannya pun  melalui pernapasan. Berdekatan dengan penderita TBC dapat memungkinkan  kita untuk tertular. Selain itu, ketika penderita TBC batuk pun, bisa  jadi itu merupakan&nbsp; sarana penularan TBC.</span></p>
<p><span style="font-size: medium;">Selain itu, penggunaan barang pribadi secara bergantian dengan  penderita TBC aktif, seperti gelas dan sendok pun dapat menjadi jembatan  penularan TBC.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Mengurangi kotak dengan penderita TBC aktif. Jika akan kontak pun,  gunakanlah masker untuk melindungi pernapasan kita. Serta hindari  penggunaan barang pribadi yang bergantian dengan penderita TBC aktif.</span></li>
<li><span style="font-size: medium;">Pemberian Vaksin BCG (diberikan pada saat balita)</span></li>
<li><span style="font-size: medium;">Menjaga pola hidup yang baik dengan asupan makanan yang bergizi dan olah raga teratur.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">3. Muntaber</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Muntaber adalah penyakit peradangan usus yang disebabkan oleh virus,  bakteri, ataupun parasit lain seperti jamur, protozoa dan cacing. Selain  karena itu, muntaber juga dapat disebabkan oleh keracunan makanan atau  minuman yang mengandung bakteri atau zat kimia. &nbsp;Bakteri yang biasanya  merupakan penyebab dari muntaber adalah bakteri Escherichia Coli.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Kondisi lingkungan, terutama sanitasi air yang tidak bersih merupakan salah satu faktor besar dalam penyebaran penyakit ini.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui cairan dari mulut (muntah) yang tidak dibersihkan dengan baik</span></li>
<li><span style="font-size: medium;">Melalui sisa kotoran yang menyebar di air yang dgunakan</span></li>
<li><span style="font-size: medium;">Melalui saluran air. Terutama jika sanitasi air di lingkungan sekitar masih buruk.</span></li>
<li><span style="font-size: medium;">Lingkungan yang tidak bersih atau sedang dalam kondisi seperti banjir yang tidak memungkinkan memiliki air bersih.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menjaga asupan makanan yang dikonsumsi secara cukup dan seimbang</span></li>
<li><span style="font-size: medium;">Penggunaan air bersih untuk kegiatan sehari-hari terutama air minum</span></li>
<li><span style="font-size: medium;">Mencuci tangan secara teratur untuk menghindari bakteri menempel pada tangan. Terutama sebelum dan setelah makan</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan rumah dan lingkungan sekitar</span></li>
<li><span style="font-size: medium;">Membuang tinja pada tempatnya dan membersihkan dengan baik</span></li>
<li><span style="font-size: medium;">Mencuci seluruh bahan makanan sebelum masuk proses pemasakan</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan peralatan makan dan minum</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">4. Cacar Air</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;"><a rel="prettyphoto[387]" href="http://halosehat.com/wp-content/uploads/2015/03/cacar-air.jpg"><img class="size-thumbnail wp-image-388 alignleft" src="http://halosehat.com/wp-content/uploads/2015/03/cacar-air-150x150.jpg" alt="cacar air" width="150" height="150"></a>Cacar air adalah penyakit yang disebabkan oleh infeksi virus <em>varicella zoster</em>yang  menimbulkan bintik kemerahan di kulit yang menggelembung maupun tidak,  melepuh, dan terasa gatal. Masa inkubasi virus penyebab cacar ini  sekitar 2-3 minggu. Biasanya awal gejala ditandai dengan naiknya suhu  tubuh.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; :</strong></span></p>
<ul>
<li><span style="font-size: medium;">Cacar air dapat menular melalui kontak langsung dengan penderita.  Seperti berjabat tangan, atau bersentuhan langsung dengan gelembung  bintik yang pecah.</span></li>
<li><span style="font-size: medium;">Cacar air juga dapat menular melalui udara. Misalnya, saat penderita  cacar bernapas, bersin, atau batuk dan terhirup oleh udara ke arah  kita, kita dapat tertular cacar air.</span></li>
<li><span style="font-size: medium;">Melalui barang pribadi penderita, seperti pakaian</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melakukan vaksinasi cacar air</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri sendiri, pakaian, dan lingkungan</span></li>
<li><span style="font-size: medium;">Mengkonsumsi makanan bergizi</span></li>
<li><span style="font-size: medium;">Menghindari sumber penularan cacar air</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">5. Tifus</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Tifus adalah penyakit infeksi pada usus halus yang disebabkan oleh  bakteri salmonella.Biasanya ditandai dengan demam yang suhunya naik  secara bertahap hingga membuat pendeita menggigil. Biasanya demam  terjadi di malam hari dan mereda, kemudian akan naik lagi di malam  berikutnya. Gejala yang lain dapat berupa &nbsp;sakit kepala, sakit di bagian  perut, denyut jantung menurun, sampai kehilangan nafsu makan.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui makanan yang tercemar bakteri salmonella. Ini bisa terjadi  karena sumber makanan yang tidak sehat ataupun pembersihan yang tidak  baik sebelum bahan makanan tersebut dimakan. Bahkan pada sebagian kasus,  ada yang disebabkan menempelnya lalat pada makanan yang sebelumnya  hinggap di tinja atau kotoran milik penderita tifus. Akhirnya lalat  tersebut menjadi perantara penularan tifus.</span></li>
<li><span style="font-size: medium;">Melalui tangan dan kuku yang tidak bersih, sehingga tanpa kita  sadari bakteri salmonella yang bisa saja terdapat pada tangan dan kuku  kita masuk ke dalam mulut.</span></li>
<li><span style="font-size: medium;">Melalui air yang digunakan untuk minum atau mencuci piring dan gelas  dan peralatan makan lainnya. Untuk itulah beberapa ahli mengatakan  bahwa <a title="9 Bahaya Air Minum Isi Ulang (Wajib diketahui)" href="http://halosehat.com/minuman/minuman-berbahaya/9-bahaya-air-minum-isi-ulang-wajib-diketahui">bahaya air minum isi ulang</a> wajib diwaspadai.</span></li>
<li><span style="font-size: medium;">Melalui kulit. Bakteri ini dapat masuk lewat kulit yang terkoyak  akibat luka. Bisa luka bekas operasi, terjauth, atau luka lainnya.</span></li>
<li><span style="font-size: medium;">Tifus juga dapat menular melalui lingkungan yang tidak bersih.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Memastikan kebersihan bahan makanan sebelum memasaknya</span></li>
<li><span style="font-size: medium;">Mencuci tangan secara teratur, terutama sebelum dan setelah makanan</span></li>
<li><span style="font-size: medium;">Membersihkan luka dan segera mengobatinya</span></li>
<li><span style="font-size: medium;">Hindari jajan di pinggir jalan yang terlihat tidak higienis</span></li>
<li><span style="font-size: medium;">Menjaga daya tahan tubuh.</span></li>
<li><span style="font-size: medium;">Memakan <a title="22 Makanan untuk Penderita Tipes" href="http://halosehat.com/makanan/makanan-sehat/22-makanan-untuk-penderita-tipes">makanan untuk penyakit tifus</a>.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">6. Campak</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;"><a rel="prettyphoto[387]" href="http://halosehat.com/wp-content/uploads/2015/03/campak.jpg"><img class="wp-image-389 size-thumbnail alignleft" src="http://halosehat.com/wp-content/uploads/2015/03/campak-150x150.jpg" alt="campak" width="150" height="150"></a>Campak  adalah penyakit menular yang disebabkan oleh virus yang termasuk ke  dalam golongan paramixovirus. Campak sangat menular. Biasanya gejalanya  berupa naiknya suhu tubuh, batuk, nyeri tenggorokan, nyeri otot, hingga  ruam pada kulit. Gejala ini muncul sekitar 7-14 hari setelah terinfeksi  virus.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<p><span style="font-size: medium;">Campak menular melalui cairan ludah dari penderita ketika batuk ataupun bersin.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<p><span style="font-size: medium;">Melakukan vaksinasi ketika masih usia balita.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;"><span style="line-height: 1.5;">7.&nbsp;</span>Pneumonia</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Pneumonia atau radang paru-paru adalah suatu peradangan yang  disebabkan oleh bakteri, virus, maupun parasit lainnya. Peradangan  terjadi pada pulmonary alveolus (alveoli) yang seharusnya bertugas untuk  menyerap oksigen dari atmosfer. Akan tetapi karena terjadinya  peradangan, organ ini menjadi terisi cairan sehinggapenyerapan oksigen  terganggu dan menyebabkan sulit bernapas. Gejalanya dmulai dari demam,  batuk, hingga mengalami kesulitan bernapas.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<p><span style="font-size: medium;">Melalui udara yang tercemar oleh bakteri, virus, atau parasit  penyebab pneumonia. Begitu juga udara yang terpapar penyebab pneumonia  yang berasal dari penderita.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Mengenakan masker atau pelindung pernapasan apabila dekat dengan sumber risiko penularan pneumonia.</span></li>
<li><span style="font-size: medium;">Menjaga daya tahan tubuh agar tetap kuat melawan semua virus atau bakteri yang masuk ke dalam tubuh.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">8. Hepatitis</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Hepatitis adalah penyakit menularyang menyerang organ hati pada  manusia. Disebabkan oleh bakteri serta virus dan tidak bersihnya  lingkungan sekitar, sehingga menginfeksi hati dan terjadi peradangan.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Penularan melalui oral atau masuknya penyebab hepatitis ke dalam saluran pencernaan melalui makanan atau minuman.</span></li>
<li><span style="font-size: medium;">Melalui cairan tubuh seperti ludah.</span></li>
<li><span style="font-size: medium;">Melalui kulit, seperti pemakaian jarum suntik bekas, alat tattoo, atau jarum akupuntur bekas penderita.</span></li>
<li><span style="font-size: medium;">Pemakaian barang pribadi bersamaan, seperti pakaian, dan peralatan makan.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menjaga kebersihan makanan dan minuman yang masuk ke dalam tubuh.</span></li>
<li><span style="font-size: medium;">Hindari pertukaran cairan tubuh, seperti ludah atau transfusi darah yang belum jelas apakah telah bebas penyakit atau tidak.</span></li>
<li><span style="font-size: medium;">Hindari pemakaian barang pribadi bersamaan seperti pakaian, alat makan, dan sikat gigi dengan penderita hepatitis.</span></li>
<li><span style="font-size: medium;">Pastikan anda menggunakan jarum baru ketika melakukan transfusi darah ataupun melakukan akupuntur.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;"><span style="line-height: 1.5;">9.&nbsp;</span>Penyakit PES</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">PES atau yang juga dikenal dengan Pesteurellosis, merupakan penyakit  pada tikusdan hewan pengerat lainnya yang disebabkan oleh bakteri dan  dapat ditularkan pada manusia. Kutu tikus adalah yang paling sering  menjadi perantara dalam penularan penyakit ini. Pada manusia, PES dapat  dibedakan menjadi . Yaitu PES Kelenjar Getah Bening, PES Infeksi Luas,  Dan PES Pneumonik atau PES Paru-paru.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Terkena gigitan kutu tikus yang sebelumnya telah menghisap darah tikus dengan penyakit PES.</span></li>
<li><span style="font-size: medium;">Melalui titik-titik air liur di udara dari penderita PES Paru-paru.</span></li>
<li><span style="font-size: medium;">Kontak langsung dengan menyentuh luka atau nanah penderita PES.</span></li>
<li><span style="font-size: medium;">Kontak tidak langsung dengan menyentuh permukaan tanah yang berbakteri.</span></li>
<li><span style="font-size: medium;">Makanan dan minuman yang tidak bersih dan tercemar bakteri.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan&nbsp;&nbsp;&nbsp;&nbsp; :</strong></span></p>
<ul>
<li><span style="font-size: medium;">Membersihkan lingkungan agar tidak terdapat tikus di sekitar rumah.</span></li>
<li><span style="font-size: medium;">Hindari kontak langsung dengan penderita PES, atau penggunaan masker dan sarung tangan jika ingin melakukan kontak langsung.</span></li>
<li><span style="font-size: medium;">Menjaga asupan makanan yang bebas dari bakteri.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">10.&nbsp;Kolera</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;"><a rel="prettyphoto[387]" href="http://halosehat.com/wp-content/uploads/2015/03/kolera.jpg"><img class="wp-image-390 size-thumbnail alignleft" src="http://halosehat.com/wp-content/uploads/2015/03/kolera-150x150.jpg" alt="kolera" width="150" height="150"></a>Kolera adalah penyakit infeksi saluran usus yang akut yang disebabkan oleh infeksi bakteri <em>Vibrio cholerae. </em>Bakteri&nbsp;  dapat masuk ke saluran pencernaan melalui makanan dan minuman yang  telah tercemar bakteri ini. Pada saluran usus, bakteri ini mengeluarkan  racunnya sehingga tubuh mengalami diare disertai muntah yang hebat.  Sebagai akibatnya, tubuh akan masuk pada tahap dehidrasi dalam waktu  yang sangat cepat.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Bakteri ini berkembang biak dan menyebar melalui kotoran manusia.  Ketika kotoran berisi bakteri ini mencemari sungai, maka orang-orang  yang kontak langsung dengan sungai tersebut dapat tertular. Atau bisa  juga melalui ikan yang hidup di sugai tersebut dan ikan itu dikonsumsi  oleh manusia.</span></li>
<li><span style="font-size: medium;">Makanan dan minuman yang tercemar baketri.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Sanitasi lingkungan yang baik. Terutama kebersihan air yang  digunakan untuk minum, mandi, mencuci pakaian, alat makan, serta bahan  makanan yang akan diolah.</span></li>
<li><span style="font-size: medium;">Hindari memasak ikan atau kerang setengah matang.</span></li>
<li><span style="font-size: medium;">Jika keluarga atau kerabat dekat ada yang terinfeksi, pisahkan  barang pribadi dan tempat tidur penderita agar tidak mudah menular pada  anggota keluarga atau kerabat lainnya.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">11. Polio</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit yang menyerang tubuh terutama pada bagian otot dan syaraf  yang dapat mengakibatkan pelemahan otot yang bersifat permanen. Akhirnya  tubuh dapat mengalami kelumpuhan bahkan hingga kematian. Penyakit ini  disebabkan oleh virus polio yang sangat menular.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Kontak langsung dengan penderita polio.</span></li>
<li><span style="font-size: medium;">Secara oral melalui ludah penderita polio.</span></li>
<li><span style="font-size: medium;">Virus masuk melalui mulut dan hidung.</span></li>
<li><span style="font-size: medium;">Bepergian ke daerah yang masih banyak terjadi polio dengan sistem daya tahan tubuh yang lemah.<strong>&nbsp;</strong></span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<p><span style="font-size: medium;">Vaksin polio ketika masih usia anak-anak.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">12. Ebola</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit yang belakangan ini menjadi perbinacangan hangat adalah  penyakit yang disebabkan oleh virus mematikan dari genus ebolavirus.  Gejala yang terjadi biasanya adalah demam, sakit kepala, nyeri otot,  muntah, dan pada akhirnya akan mengakibatkan peradangan hati, rusaknya  ginjal, serta turunnya jumlahtrombosit secara drastis. Sampai saat ini  vaksin untuk ebola belum dapat ditemukan.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<p><span style="font-size: medium;">Kontak langsung dengan penderita, atau melalui cairan tubuh.</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Hindari bepergian ke daerah rawan Ebola</span></li>
<li><span style="font-size: medium;">Menghindari kontak langsung dengan penderita. Aplagi terkena cairan tubuhnya seperti ludah, kotoran, ataupun keringat.</span></li>
<li><span style="font-size: medium;">Jika terpaksa harus kontak angsung dengn penderita, gunakan pengaman tubuh seperti sarung tangan dan masker.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">13. AIDS (Acquired Immuno Deficiency Syndorme)</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">AIDS adalah penyakit yang menyerang pada sel-sel darah putih yang  bertugas untuk membentuk kekebalan tubuh. Akibatnya, daya tahan tubuh  menjadi merosot dan sangat mudah dihinggapi berbagai macam penyakit.  AIDS dapat menyebabkan kematian.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui hubugan seksual dengan penderita AIDS</span></li>
<li><span style="font-size: medium;">Melalui cairan tubuh</span></li>
<li><span style="font-size: medium;">Melalui transfusi darah</span></li>
<li><span style="font-size: medium;">Ditularkan oleh ibu yang tengah mengandung pada bayi yang dikandungnya.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Hindari kontak dengan cairan tubuh penderita AIDS, seperti sperma,  air liur, air seni, darah, dan cairan tubuh penderita lainnya.</span></li>
<li><span style="font-size: medium;">Bagi wanita hamil, jauhkanlah diri dari oenderita AIDS, karena akan sangat berbahaya bagi dirinya dan bayi yang dikandungnya.</span></li>
<li><span style="font-size: medium;">Pemisahan benda-benda pribadi dengan penderita AIDS.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">14. DBD (Demam Berdarah Dengue)</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">DBD adalah penyakit menular yang disebabkan oleh virus dengue yang dibawa oleh nyamuk <em>Aedes aegeypti </em>Betina.  Gejala yang umum terjadi adalah demam tinggi pada beberapa hari, sakit  pada persendian, munculnya bintik-bintik merah, turunnya trombosit  secara drastis, dan bisa terjadi pendarahan.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<p><span style="font-size: medium;">Ditularkan melalui gigitan nyamuk betina Aedes aegepty betina</span></p>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Membersihkan genangan air di sekitar rumah agar terbebas dari nyamuk Aedes aegepty.</span></li>
<li><span style="font-size: medium;">Menutup tempat-tempat penyimpanan air.</span></li>
<li><span style="font-size: medium;">Menguras bak mandi minimal satu minggu sekali</span></li>
<li><span style="font-size: medium;">Memebersihkan pekarangan rumah dari barang-barang bekas yang berpotensi sebagai tempat perkembangan jentik nyamuk.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">15. Rabies</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit rabies adalah penyakit yang menyerang sistem syaraf pusat  pada manusia maupun hewan berdarah panas. Penyakit ini disebabkan oleh  virus rabies, dan ditularkan pada manusia melalui hewan (anjing, kucing,  atau kera).</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui gigitan hewan atau luka terbuka.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menghindari gigitan hewan atau luka terbuka pada hewan.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">16. Panu</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Tidak disangka penyakit kulit yang sering dianggap ringan ini adalah  penyakit menular. Panu menjadikan kulit kita memiliki bercak-bercak  putih yang kadang terasa gatal.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; :</strong></span></p>
<ul>
<li><span style="font-size: medium;">Bisa karena jamur di handuk lembab ataupun tidak menjaga kebersihan tubuh.</span></li>
<li><span style="font-size: medium;">Tertular dari penderita panu lain.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menjaga kebersihan tubuh. Cara termudah adalah dengan mandi setiap hari.</span></li>
<li><span style="font-size: medium;">Menghindari pemakaian handuk secara bergantian.</span></li>
<li><span style="font-size: medium;">Menjemur handuk setelah dipakai</span></li>
<li><span style="font-size: medium;">Mencuci handuk minimal satu minggu sekali.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">17. Malaria</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Malaria adalah penyakit yang disebabkan oleh infeksi parasit  plasmodium. Parasit ini dibawa dan disebarkan oleh nyamuk anopheles.  Penderita akan mengalami demam tinggi, menggigil, nyeri bagian tubuh  serta mual hingga muntah-muntah.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui nyamuk anopheles.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menghindari gigitan nyamuk dengan berbagai cara. Memakai pakaian panjang, atau menggunakan kelambu jika berada di rumah.</span></li>
<li><span style="font-size: medium;">Konsultasikan dahulu dengan dokter sebelum bepergian ke daerah yang marak terjadi malaria.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">18. Cacingan</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Cacingan adalah penyakit yang masih marak di Indonesia. Hal ini dapat  disebabkan karena kurangnya kesadaran dalam menerapkan pola hidup sehat  dan sanitasi yang buruk. Pada penderita cacingan, akan ditemukan cacing  pada tubuhnya, biasanya cacing ini dapat dikeluarkan lewat buang air  besar, ataupun dari mulut dan hidung.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Telur cacing dapat masuk melalui kuku dan tangan yang tidak bersih</span></li>
<li><span style="font-size: medium;">Makanan yang ridak dibersihkan dengan baik sebelum dimasak</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Mencuci tangan dengan baik setelah dan sebelum beraktifitas di luar rungan, begitu juga ketika akan makan.</span></li>
<li><span style="font-size: medium;">Memastikan kebersihan makanan sebelum dimasak</span></li>
<li><span style="font-size: medium;">Memotong kuku dan tidak membiasakan memanjangkan kuku</span></li>
<li><span style="font-size: medium;">Rutin membersihkan WC setiap hari</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri sendiri dan lingkungan</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">19. Toksoplasmosis</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;"><a rel="prettyphoto[387]" href="http://halosehat.com/wp-content/uploads/2015/03/Toksoplasmosis.jpg"><img class="wp-image-391 size-thumbnail alignleft" src="http://halosehat.com/wp-content/uploads/2015/03/Toksoplasmosis-150x150.jpg" alt="toksoplasmosis" width="150" height="150"></a>Penyakit  ini disebabkan oleh parasit jenis protozoa dari kelas Toxoplasmea.  Infeksi parasit ini menyebabkan peradangan bada beberapa oran tubuh  seperti kulit, kelenjar getah bening, jantung, paru-paru, bahkan otak  dan selaput otak. Biasanya ditularkan melalui hewan seperti kucing,  anjing dan hewan lainnya.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui tinja kucing atau kotoran hewan perantara lain yang terkontaminasi ookista toxoplasma.</span></li>
<li><span style="font-size: medium;">Kontak langsung kulit dengan hewanperantara yang terluka.</span></li>
<li><span style="font-size: medium;">Penularan dari ibu hamil kepada bayi yang masih dikandungnya.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Memasak makanan dan minuman dengan sempurna</span></li>
<li><span style="font-size: medium;">Mengobati atau menghindari hewan perantara yang sakit</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri sendiri dan lingkungan sekitar</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">20. Disentri Hasiler</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Bakteri patogen menjadi penyebab infeksi pada usus besar dan  menjadikan tubuh terkena disentri hasiler. Gejala wal biasanya demam  tinggi, mual muntah, diare hebat hingga keluar lendir dan darah  bersamaan dengan kotoran.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Karena kuman dan bakteri patogen yang masuk ke dalam sistem pencernaan melalui mulut.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Memastikan seluruh makanan yang akan dimakan bersih</span></li>
<li><span style="font-size: medium;">Mencuci tangan secara teratur terutama setelah beraktifitas, dan sebelum juga setelah makan</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri dan lingkungan</span></li>
<li><span style="font-size: medium;">Memasak air minum</span></li>
<li><span style="font-size: medium;">Tidak menggunakan air mentah dalam masakan</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">21. Tetanus</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Tetanus disebabkan oleh kuman Clostridium tetani yang menginfeksi  luka yang terbuka. Biasanya menimbulkan kejang-kejang yang dapat  berulang.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui luka yang terbuka atau terdapat benda-benda asing pada luka tersebut.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Membersihkan luka sesegera mungkin</span></li>
<li><span style="font-size: medium;">Mengobati luka</span></li>
<li><span style="font-size: medium;">Imunisasi</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">22. Konjungtivitis (Penyakit Mata Merah)</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit yang menyebabkan mata memerah karena infeksi bakteri.  Biasanya menyebabkan mata menjadi bengkak, terasa sakit, dan  mengeluarkan kotoran dalam jumlah banyak yang biasanya berwarna kuning  atau kehijauan.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Kontak langsung dengan penderita konjungtivitis. Misalnya tangan  penderita dipakai mengusap matanya, lalu dipakai berjabat tangan dengan  kita dan tanga kita menyentuh mata kita sendiri.</span></li>
<li><span style="font-size: medium;">Virus yang terbawa oleh udara. Karena itu penderita konjungtivitis  ini disarankan beristirahat di rumah dan menghindari bertemu orang  banyak agar tidak menulari orang lain.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Hindari kontak langsung dengan penderita kongjungtivitis</span></li>
<li><span style="font-size: medium;">Memelihara kesehatan mata</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">23. SARS (Severe Acute Respiratory Syndrome)</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">SARS adalah gangguan infeksi akut pada sistem pernapasan yang dapat menyebabkan kematian pada penderitanya.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Virus ini dapat menyebar melalui udara.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Hindari berdekatan apalagi kontak langsung secara intensif dengan penderita SARS</span></li>
<li><span style="font-size: medium;">Pemakaian masker sebagi pelindung pernapasan</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;"><strong style="line-height: 1.5;">24. Rubella</strong></span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Rubella adalah campak Jerman yang disebabkan oleh virus rubella.  Umumnya penyakit ini menjangkiti anak-anak dan remaja. Gejala yang  dialami penderita rubella biasanya adalah demam, iritasi ringan pada  mata, hidung tersumbat, mual dan mntah, munculnya ruam di kulit yang  menyebar, membengkaknya kelenjar getah bening, sertanyeri sendi.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menular melalui titik-titik air di udara yang berasal dari batuk atau bersin penderita rubella.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Hindari kontak dengan penderita rubella, terutama ibu hamil.</span></li>
<li><span style="font-size: medium;">Pindahkan penderita ke ruangan terpisah dari keluarga dan kerabat</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri sendiri.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">25. Flu Burung (H5N1)</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit menular yang disebabkan oleh virus H5N1 pernah jadi  perbincangan yang hebat karena efeknya yang dahsyat menelan korban para  penderitanya. Virus yang awalnya berasal dari unggas ini dapat menyerang  pada manusia. Biasanya pada manusia akan menyerang pada sistem  pernapasan dengan gejala awal demam, dan dapat menyebabkan kematian jika  tidak ditangani.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Dapat terjadi dari hewan ke hewan</span></li>
<li><span style="font-size: medium;">Dapat terjadi dari hewan ke manusia melalui udara</span></li>
<li><span style="font-size: medium;">Juga melalui kontak langsung antara manusia dan unggas yang terinfeksi</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Bagi yang sering bersentuhan dengan unggas, baiknya mencuci tangan dengan disinfektan segera setelah bersentuhan dengan unggas.</span></li>
<li><span style="font-size: medium;">Menghindari kontak langsung dengan unggas yang terinfeksi</span></li>
<li><span style="font-size: medium;">Menggunakan masker dan sarung tanga sebagai pelindung jika terpaksa kontak langsung</span></li>
<li><span style="font-size: medium;">Imunisasi</span></li>
<li><span style="font-size: medium;">Memilih unggas yang sehat untuk dimasak</span></li>
<li><span style="font-size: medium;">Memasak daging unggas pada suhu mencapai 80 derajat celcius.</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri sendiri dan lingkungan</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">26. Demam Chikungunya</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Chikugunya adalah penyakit yang ditandai dengan gejala demam, nyeri  pada sendi, ruam pada kulit, dan tubuh lunglai dan lemas tidak seperti  biasanya. Penyakit ini disebabkan oleh gigitan nyamuk Aedes aegepty yang  membawa virus dari famili Togaviridae.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Penyakit ini tidak menular dari manusia ke manusia. Akan tetapi  menular apabila manusia telah digigit oleh nyambuk yang membawa virus  penyebab chikungunya.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Membersihkan genangan air di sekitar rumah agar terbebas dari nyamuk Aedes aegepty.</span></li>
<li><span style="font-size: medium;">Menutup tempat-tempat penyimpanan air.</span></li>
<li><span style="font-size: medium;">Menguras bak mandi minimal satu minggu sekali</span></li>
<li><span style="font-size: medium;">Memebersihkan pekarangan rumah dari barang-barang bekas yang berpotensi sebagai tempat perkembangan jentik nyamuk.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">27. Leishmaniasis</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit ini disebabkan oleh protozoa Leishmania yang dapat  mengganggu organ bagian dalam juga mengakibatkan luka pada kulit.  Penyakit ini ditularkan melalui gigitan lalat pasir. Jika Leishmaniasis  mengganggu organ dalam, akibatnya bisa sangat fatal. Biasanya organ yang  terkena adalah limpa, hati, dan sumsum tulang. Sedangkat pada kulit,  luka kecil yang disebabkan Leishmaniasis, dapat semakin besar dan  menjadi luka terbuka.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melalui gigitan lalat pasir</span></li>
<li><span style="font-size: medium;">Pada kasus yang jarang ditemukan, penularan juga dapat terjadi  melalui hubungan seksual dan penularan dari ibu hamil kepad bayi yang  dikandungnya.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menghindari gigitan lalat pasir</span></li>
<li><span style="font-size: medium;">Berhati-hati terhadap hubungan seksual dengan penderita Leishmaniasis</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;"><span style="line-height: 1.5;">28.&nbsp;</span>Demam Kuning</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Demam kuning adalah penyakit sistemik akut yang disebabkan oleh  virus. Virus demam kuning ini dibawa dan ditularkan oleh nyamuk, salah  satunya adalah Aedes aegepty. Meskipun penyakit ini belum ditemukan di  Indonesia, akan tetapi vaksin demam kuning sangat dianjurkan WHO dan  Kementrian Kesehatan RI jika ingin melakukan perjalanan internasional.  Biasanya penyakit ini ditemukan di daerah Afrika dan beberapa negara di  Amerika Latin.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit ini dapat merusak saluran hati, ginjal, jantung dan sistem  pencernaan. Gejala yang biasa terjadi adalah demam dan mual. Pada tahap  berikutnya, racun akan menyebar. Penyebaran racun ini ditandai dengan  warna kulit menjadi kekuningan karena kerusakan hati. Kemudian dapat  berlanjut pada gagal ginjal, meningitis, kemudian menyebabkan kematian.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menular melalui nyamuk. Salah satunya adalah Aedes aegepty, meskipun  masih&nbsp; terdapat spesies lain yang juga menjadiperantara penularan.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Melakukan vaksinasi demam kuning</span></li>
<li><span style="font-size: medium;">Memakai pakaian panjang dan menutupi tubuh jika bepergian ke daerah yang terdapat risiko demam kuning.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">28. Roseola Infatum</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit ini adalah penyakit yang menyerang pada bayi atau anak-anak  yang sangat muda. Penyakit ini disebabkan oleh virus dan mengakibatkan  demam tinggi dan ruam pada bayi atau anak.&nbsp; Terkadang disertai dengan  pembengkakan kelenjar getah bening.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Karena penyakit ini disebabkan oleh virus, maka penyakit ini dapat menular melalui percikan air liur penderita.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Meningkatkan dan menjaga daya tahan tubuh anak dengan pemberian makanan bergizi yang cukup dan seimbang.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">29. Anthrax</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Anthrax adalah penyakit hewan yang bersifat akut dan dapat menular  pada manusia. Gejala yang dialami iasnya adalah halusinasi, serta  terganggunya sistem pernapasan. &nbsp;Selain itu anthrax juga kadang  menimbulkan bisul yang apabila pecah, akan meninggalkan bekas.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Menular pada manusia ketika manusia memakan daging hewan yang telah terkena penyakit anthrax.</span></li>
<li><span style="font-size: medium;">Selain memakan daging, kontak langsung dengan hewan berpenyakit ini pun dapat menjadikan manusia tertular.</span></li>
<li><span style="font-size: medium;">Terpapar tanah yang tercemar oleh bakteri</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Berhati-hati dalam memilih daging yang akan dikonsumsi.</span></li>
<li><span style="font-size: medium;">Hindari kontak langsung dengan hewan yang berpenyakit</span></li>
<li><span style="font-size: medium;">Menjaga kebersihan diri dari kotoran di lingkungan sekitar</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<h2 style="text-align: justify;"><span style="font-size: medium;">30. Leptospirosis</span></h2>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Penyakit ini adalah penyakit yang disebabkan oleh kuman leptospira  yang menyerang hewan namun dapat juga menular pada manusia.&nbsp; Gejala awal  leptospirosis sulit dibedakan dengan influenza karena mengalami hal  yang mirip. Seperti demam, sakit kepala, batuk, sakit tenggorokan, dan  nyeri pada sendi. Akan tetapi pada leptospirosis terkadang disertai  dengan diare dan mual muntah.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<p style="text-align: justify;"><span style="font-size: medium;">Leptospirosis menjadi berbahaya apabila kuman ini menjalar ke organ lain dan merusak selaput otak, ginjal, bahkan paru-paru.</span></p>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Penularan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Penyakit ini menular melalui hewan yang sedang sakit. Menular ke  manusia dengan cara konta k dengan hewan tersebut terutama air seni  tikus.</span></li>
</ul>
</div>
<p style="text-align: justify;"><span style="font-size: medium;"> </span></p>
<p><span style="font-size: medium;"> </span></p>
<div class="omsc-last omsc-one-half" style="text-align: justify;">
<p><span style="font-size: medium;"><strong>Cara Pencegahan</strong></span></p>
<ul>
<li><span style="font-size: medium;">Pola hidup sehat</span></li>
<li><span style="font-size: medium;">Mewaspadai air seni tikus maupun hewan lainnya</span></li>
<li><span style="font-size: medium;">Membersihkan kandang hewan peliharaan secara teratur (bila memiliki peliharaan)</span></li>
<li><span style="font-size: medium;">Membersihkan diri ketika telah bersentuhan dengan hewan</span></li>
</ul>
</div>
